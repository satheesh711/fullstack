//while loop

//1.print 1 to 7 using while loop

var i=1
while(i<=7)
{
    console.log(i)
    i++
}

// 2.print 10 to 6 in decrease order using while loop

var i=10
while(i>=6)
{
    console.log(i)
    i--
}


//do while loop

//1.print 1 to 5 using do while loop

var i=1

do{
    console.log(i)
    i++
}while(i<=5)


